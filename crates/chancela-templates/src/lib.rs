/// Render a [`TemplateSpec`] against a record context into the frozen [`DocumentModel`] seam.
///
/// `ctx` is the record(s) serialized to JSON (as `chancela-api` produces via
/// `serde_json::to_value` + derived helpers). The renderer reads these **reserved metadata keys**
/// for the document envelope, everything else is referenced by the block templates:
///
/// - `entity.name` → [`DocumentModel::entity_name`], `entity.nipc` → [`DocumentModel::entity_nipc`]
/// - `title` → [`DocumentModel::title`] (**required**), `subject` → [`DocumentModel::subject`]
///   (falls back to `title`)
/// - `created_at` → [`DocumentModel::created_at`] (ISO-8601; the caller supplies it — core reads
///   no clock)
///
/// minijinja filters registered for authors: `long_date` (`"2026-07-08"` → `"8 de julho de
/// 2026"`), `channel_label` (`MeetingChannel` name → PT label), `role_label`
/// ([`chancela_core::SignatoryCapacity`] name → PT role label). Built-in filters (`join`, …) are
/// available too.
pub fn render(spec: &TemplateSpec, ctx: &Value) -> Result<DocumentModel, RenderError> {
    let env = build_env();

    let title = ctx
        .get("title")
        .and_then(Value::as_str)
        .ok_or_else(|| RenderError::MissingField("title".to_string()))?
        .to_string();
    let subject = ctx
        .get("subject")
        .and_then(Value::as_str)
        .unwrap_or(&title)
        .to_string();
    let entity_name = ctx
        .pointer("/entity/name")
        .and_then(Value::as_str)
        .unwrap_or("")
        .to_string();
    let entity_nipc = ctx
        .pointer("/entity/nipc")
        .and_then(Value::as_str)
        .map(str::to_string);
    let created_at = ctx
        .get("created_at")
        .and_then(Value::as_str)
        .map(str::to_string);

    let mut doc = DocumentModel {
        title,
        entity_name,
        entity_nipc,
        subject,
        language: spec.locale.clone(),
        created_at,
        blocks: Vec::new(),
    };

    for block in &spec.blocks {
        render_block(&env, block, ctx, &mut doc.blocks)?;
    }

    Ok(doc)
}

/// Assemble the minijinja environment with the author-facing filters. Kept per-call so rendering
/// is stateless and thread-safe; the templates are tiny so compilation cost is negligible.
fn build_env() -> minijinja::Environment<'static> {
    let mut env = minijinja::Environment::new();
    env.add_filter("long_date", long_date);
    env.add_filter("channel_label", channel_label);
    env.add_filter("role_label", role_label);
    env
}

fn render_block(
    env: &minijinja::Environment<'_>,
    block: &BlockSpec,
    ctx: &Value,
    out: &mut Vec<Block>,
) -> Result<(), RenderError> {
    match block {
        BlockSpec::Heading { level, template } => {
            let text = render_str(env, template, ctx)?;
            if !text.trim().is_empty() {
                out.push(Block::Heading {
                    level: *level,
                    text,
                });
            }
        }
        BlockSpec::Paragraph {
            items: None,
            template,
        } => {
            push_paragraph(env, template, ctx, out)?;
        }
        BlockSpec::Paragraph {
            items: Some(path),
            template,
        } => {
            for item in array_at(ctx, path) {
                let scope = merge_scope(ctx, item);
                push_paragraph(env, template, &scope, out)?;
            }
        }
        BlockSpec::KeyValue { items: None, rows } => {
            let kvrows = render_kv_rows(env, rows, ctx)?;
            if !kvrows.is_empty() {
                out.push(Block::KeyValue { rows: kvrows });
            }
        }
        BlockSpec::KeyValue {
            items: Some(path),
            rows,
        } => {
            let mut kvrows = Vec::new();
            for item in array_at(ctx, path) {
                let scope = merge_scope(ctx, item);
                kvrows.extend(render_kv_rows(env, rows, &scope)?);
            }
            if !kvrows.is_empty() {
                out.push(Block::KeyValue { rows: kvrows });
            }
        }
        BlockSpec::VoteTable {
            items,
            label,
            vote_field,
            unanimous_total,
        } => {
            let mut vrows = Vec::new();
            for item in array_at(ctx, items) {
                let vote = item.get(vote_field);
                let scope = merge_scope(ctx, item);
                if let Some(row) =
                    project_vote(env, label, vote, unanimous_total.as_deref(), &scope)?
                {
                    vrows.push(row);
                }
            }
            if !vrows.is_empty() {
                out.push(Block::VoteTable { rows: vrows });
            }
        }
        BlockSpec::SignatureBlock { source, role, name } => {
            let mut slots = Vec::new();
            for item in array_at(ctx, source) {
                let scope = merge_scope(ctx, item);
                let role = render_str(env, role, &scope)?;
                let name = render_str(env, name, &scope)?;
                if role.trim().is_empty() && name.trim().is_empty() {
                    continue;
                }
                slots.push(SignatureSlot { role, name });
            }
            if !slots.is_empty() {
                out.push(Block::SignatureBlock { slots });
            }
        }
        BlockSpec::PageBreak => out.push(Block::PageBreak),
        BlockSpec::Rule => out.push(Block::Rule),
    }
    Ok(())
}

fn push_paragraph(
    env: &minijinja::Environment<'_>,
    template: &str,
    scope: &Value,
    out: &mut Vec<Block>,
) -> Result<(), RenderError> {
    let text = render_str(env, template, scope)?;
    if text.trim().is_empty() {
        return Ok(());
    }
    // v1 emits a single unstyled run per paragraph; inline bold/italic markup is a future
    // enhancement (the seam already carries `Run{bold,italic}` so it is additive).
    out.push(Block::Paragraph {
        runs: vec![Run {
            text,
            bold: false,
            italic: false,
        }],
    });
    Ok(())
}

fn render_kv_rows(
    env: &minijinja::Environment<'_>,
    rows: &[KvRowSpec],
    scope: &Value,
) -> Result<Vec<KvRow>, RenderError> {
    let mut out = Vec::new();
    for r in rows {
        let value = render_str(env, &r.value, scope)?;
        if value.trim().is_empty() {
            continue; // optional/absent field → omit the row
        }
        let key = render_str(env, &r.key, scope)?;
        out.push(KvRow { key, value });
    }
    Ok(out)
}

/// Project a serialized `VoteResult` into a [`VoteRow`]. Returns `None` when the element carries
/// no vote (so a deliberation with no recorded tally produces no table row).
fn project_vote(
    env: &minijinja::Environment<'_>,
    label_tmpl: &str,
    vote: Option<&Value>,
    unanimous_total: Option<&str>,
    scope: &Value,
) -> Result<Option<VoteRow>, RenderError> {
    let vote = match vote {
        Some(v) if !v.is_null() => v,
        _ => return Ok(None),
    };
    let label = render_str(env, label_tmpl, scope)?;

    // `VoteResult` is externally tagged: `Unanimous` → the string "Unanimous";
    // `Recorded{..}` → `{"Recorded": {"em_favor":_, "contra":_, "abstencoes":_}}`.
    let (favor, against, abstain) = if vote.as_str() == Some("Unanimous") {
        let total = match unanimous_total {
            Some(t) => render_str(env, t, scope)?
                .trim()
                .parse::<u32>()
                .unwrap_or(0),
            None => 0,
        };
        (total, 0, 0)
    } else if let Some(rec) = vote.get("Recorded") {
        let count = |k: &str| rec.get(k).and_then(Value::as_u64).unwrap_or(0) as u32;
        (count("em_favor"), count("contra"), count("abstencoes"))
    } else {
        (0, 0, 0)
    };

    Ok(Some(VoteRow {
        label,
        favor,
        against,
        abstain,
    }))
}

/// Render a template string against a JSON scope, mapping any minijinja error into
/// [`RenderError::Template`] with the full error chain.
fn render_str(
    env: &minijinja::Environment<'_>,
    src: &str,
    scope: &Value,
) -> Result<String, RenderError> {
    env.render_str(src, scope)
        .map_err(|e| RenderError::Template(format!("{e:#}")))
}

/// Follow a dotted path (`"a.b"`) to an array in `ctx`; return its elements, or empty if the path
/// is absent or not an array.
fn array_at<'a>(ctx: &'a Value, path: &str) -> Vec<&'a Value> {
    let mut cur = ctx;
    for seg in path.split('.') {
        match cur.get(seg) {
            Some(v) => cur = v,
            None => return Vec::new(),
        }
    }
    cur.as_array()
        .map(|a| a.iter().collect())
        .unwrap_or_default()
}

/// Overlay an array element's fields on the parent context so a per-element template can read both
/// the element (`{{ text }}`) and the record root (`{{ members_present }}`). The element wins on a
/// key clash.
fn merge_scope(parent: &Value, item: &Value) -> Value {
    match (parent.as_object(), item.as_object()) {
        (Some(p), Some(i)) => {
            let mut m = p.clone();
            for (k, v) in i {
                m.insert(k.clone(), v.clone());
            }
            Value::Object(m)
        }
        _ => item.clone(),
    }
}

// --- author-facing minijinja filters (reusable helpers, not per-template code) ----------------

/// `"2026-07-08"` → `"8 de julho de 2026"`. Passes the input through unchanged if it is not a
/// `YYYY-MM-DD` date, so it is safe on already-formatted or empty values.
fn long_date(value: String) -> String {
    const MESES: [&str; 12] = [
        "janeiro",
        "fevereiro",
        "março",
        "abril",
        "maio",
        "junho",
        "julho",
        "agosto",
        "setembro",
        "outubro",
        "novembro",
        "dezembro",
    ];
    let parts: Vec<&str> = value.split('-').collect();
    if parts.len() == 3 {
        if let (Ok(y), Ok(m), Ok(d)) = (
            parts[0].parse::<i32>(),
            parts[1].parse::<usize>(),
            parts[2].parse::<u32>(),
        ) {
            if (1..=12).contains(&m) {
                return format!("{d} de {} de {y}", MESES[m - 1]);
            }
        }
    }
    value
}

/// [`chancela_core::MeetingChannel`] serialized name → PT label.
fn channel_label(value: String) -> String {
    match value.as_str() {
        "Physical" => "Presencial",
        "Hybrid" => "Híbrida (presencial e telemática)",
        "Telematic" => "Telemática",
        "WrittenResolution" => "Deliberação unânime por escrito",
        other => other,
    }
    .to_string()
}

/// [`chancela_core::SignatoryCapacity`] serialized name → PT role label.
fn role_label(value: String) -> String {
    match value.as_str() {
        "Chair" => "Presidente da mesa",
        "Secretary" => "Secretário",
        "Member" => "Membro",
        "Manager" => "Gerente",
        "Administrator" => "Administrador",
        "Attorney" => "Mandatário",
        "CondoOwner" => "Condómino",
        other => other,
    }
    .to_string()
}


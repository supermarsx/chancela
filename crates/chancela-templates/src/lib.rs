    /// Flatten every rendered block into one string for cheap catalog-wide text assertions.
    fn doc_text(doc: &DocumentModel) -> String {
        let mut s = String::new();
        for b in &doc.blocks {
            match b {
                Block::Heading { text, .. } => {
                    s.push_str(text);
                    s.push(' ');
                }
                Block::Paragraph { runs } => {
                    for r in runs {
                        s.push_str(&r.text);
                        s.push(' ');
                    }
                }
                Block::KeyValue { rows } => {
                    for r in rows {
                        s.push_str(&r.key);
                        s.push(' ');
                        s.push_str(&r.value);
                        s.push(' ');
                    }
                }
                Block::VoteTable { rows } => {
                    for r in rows {
                        s.push_str(&r.label);
                        s.push(' ');
                    }
                }
                Block::SignatureBlock { slots } => {
                    for slot in slots {
                        s.push_str(&slot.role);
                        s.push(' ');
                        s.push_str(&slot.name);
                        s.push(' ');
                    }
                }
                Block::PageBreak | Block::Rule => {}
            }
        }
        s
    }


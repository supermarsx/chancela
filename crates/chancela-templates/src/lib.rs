/// The loaded, validated template catalog. Holds the specs keyed by id (id-uniqueness enforced at
/// load), with lookup by id and filter by (family, stage).
#[derive(Debug, Clone, Default)]
pub struct Registry {
    specs: Vec<TemplateSpec>,
}

impl Registry {
    /// All specs, in load order (sorted by asset filename — deterministic).
    pub fn specs(&self) -> &[TemplateSpec] {
        &self.specs
    }

    /// Look a spec up by its versioned id.
    pub fn get(&self, id: &str) -> Option<&TemplateSpec> {
        self.specs.iter().find(|s| s.id == id)
    }

    /// All specs matching a (family, stage) — the template picker's filter (`GET /v1/templates`).
    pub fn find(&self, family: EntityFamily, stage: LifecycleStage) -> Vec<&TemplateSpec> {
        self.specs
            .iter()
            .filter(|s| s.family == family && s.stage == stage)
            .collect()
    }
}

/// Errors from loading the embedded template registry.
#[derive(Debug, thiserror::Error)]
pub enum RegistryError {
    /// Two specs declared the same id (§3.2: the loader rejects duplicates).
    #[error("duplicate template id: {0}")]
    DuplicateId(String),
    /// An embedded asset failed to parse.
    #[error("failed to parse template asset {asset}: {source}")]
    Asset {
        /// The offending asset name (file stem).
        asset: String,
        /// The underlying parse error.
        #[source]
        source: serde_json::Error,
    },
}

/// Errors from rendering a [`TemplateSpec`] into a [`DocumentModel`].
#[derive(Debug, thiserror::Error)]
pub enum RenderError {
    /// A minijinja template failed to render (bad syntax or a strict-mode miss).
    #[error("template render failed: {0}")]
    Template(String),
    /// The record context was missing a field the renderer required (e.g. the document title).
    #[error("missing context field: {0}")]
    MissingField(String),
}

/// Load the embedded template catalog (§3.2): parse every `assets/*.json`, reject duplicate ids,
/// return the [`Registry`].
pub fn load_registry() -> Result<Registry, RegistryError> {
    registry_from(ASSET_FILES)
}

/// Build a registry from an explicit `(asset_name, json)` list. Factored out of [`load_registry`]
/// so tests can exercise duplicate-id rejection without shipping a broken asset.
fn registry_from(assets: &[(&str, &str)]) -> Result<Registry, RegistryError> {
    let mut specs: Vec<TemplateSpec> = Vec::with_capacity(assets.len());
    for (name, json) in assets {
        let dto: TemplateSpecDto =
            serde_json::from_str(json).map_err(|source| RegistryError::Asset {
                asset: (*name).to_string(),
                source,
            })?;
        let spec: TemplateSpec = dto.into();
        if specs.iter().any(|s| s.id == spec.id) {
            return Err(RegistryError::DuplicateId(spec.id));
        }
        specs.push(spec);
    }
    Ok(Registry { specs })
}


/// A registry entry: one template, versioned, binding a family × stage to an ordered block
/// layout (§3.2). `id` carries a version suffix (e.g. `"csc-ata-ag/v1"`); the loader rejects
/// duplicate ids. Recorded verbatim in the `document.generated` ledger event, so a later template
/// edit never changes what a past seal produced (D4).
#[derive(Debug, Clone, PartialEq, Serialize)]
pub struct TemplateSpec {
    /// Stable, versioned id (e.g. `"csc-ata-ag/v1"`). Unique across the registry.
    pub id: String,
    /// The entity family this template serves.
    pub family: EntityFamily,
    /// Where in the book lifecycle this template sits.
    pub stage: LifecycleStage,
    /// The meeting channels this template is valid for.
    pub channels: Vec<MeetingChannel>,
    /// The signature policy hint to prefer for documents rendered from this template (TPL-04).
    pub signature_policy: SignaturePolicyHint,
    /// The compliance rule pack bound to this template (TPL-30); the id, not the pack itself.
    pub rule_pack_id: String,
    /// Structured law references derived from stable catalog metadata. These are citation anchors
    /// for discovery/insertion only: they are not exhaustive and do not claim legal verification.
    pub law_references: Vec<TemplateLawReference>,
    /// The ordered block layout that produces the [`DocumentModel`].
    pub blocks: Vec<BlockSpec>,
    /// The locale the prose is authored in (v1: `"pt-PT"`, UX-21).
    pub locale: String,
    /// The editable **seed body** an instrument (a termo — and, later, an ata) is drafted with
    /// before an operator fills it in. Empty for templates with no fillable body.
    ///
    /// This is deliberately **not** part of the rendered [`blocks`](Self::blocks) layout and
    /// **not** part of [`canonical_spec_json`]: the seed does not determine any sealed bytes
    /// (the operator's edited body does), so editing a seed must neither read as a change to a
    /// template's render identity nor drift a shipped template's frozen digest. Hence
    /// `#[serde(skip)]`. Read it through [`TemplateSpec::default_body`].
    #[serde(skip)]
    pub default_body: Vec<DefaultBodyClause>,
}

impl TemplateSpec {
    /// The template's editable seed body — the clauses a fresh instrument's body is seeded
    /// with. Empty when the template has no fillable body.
    ///
    /// Shared accessor: the API layer seeds a drafted
    /// [`chancela_core::termo::TermoInstrument`] from these clauses (mapping each into a
    /// `TermoClause::from_template`), and the ata body editor reuses the same source. Returning
    /// a slice keeps callers insulated from the backing representation.
    #[must_use]
    pub fn default_body(&self) -> &[DefaultBodyClause] {
        &self.default_body
    }
}

/// One clause of a template's **seed body** ([`TemplateSpec::default_body`]).
///
/// A leaner mirror of [`chancela_core::termo::TermoClause`] carrying only what an asset author
/// writes — an optional heading and the plain seed text. Clause ids and provenance are minted
/// by the API layer when it seeds an instrument, not authored here. The text is rendered as a
/// *value* and never compiled as a template.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct DefaultBodyClause {
    /// Optional heading rendered above the clause.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub heading: Option<String>,
    /// The plain seed text.
    pub text: String,
}

/// The **canonical serialization** of a template spec — the exact bytes a spec digest is taken
/// over (t74 §8 hardening).
///
/// ## Why a canonical form rather than plain `to_string`
///
/// The digest has to be stable for *the same template* across builds and machines, and different
/// for *a changed template*. Serializing the struct directly would satisfy the second but not the
/// first: `serde_json` emits struct fields in declaration order, so merely **reordering fields in
/// the Rust source** — a refactor with no behavioural content — would change every digest and
/// falsely accuse every shipped template of having been edited.
///
/// Round-tripping through [`serde_json::Value`] removes that coupling: this workspace does not
/// enable serde_json's `preserve_order` feature, so `Value::Object` is a `BTreeMap` and keys
/// re-serialize in sorted order regardless of declaration order. Sequence order is preserved,
/// which is correct — `blocks` is an *ordered* layout and reordering it genuinely is a different
/// template.
///
/// The digest itself is taken by the caller (the API hashes these bytes with SHA-256), so this
/// crate stays free of a hashing dependency.
pub fn canonical_spec_json(spec: &TemplateSpec) -> Result<String, serde_json::Error> {
    let value = serde_json::to_value(spec)?;
    serde_json::to_string(&value)
}

/// Where a template law reference came from.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
pub enum TemplateLawReferenceSource {
    /// The reference is attached to the template's `rule_pack_id`.
    RulePack,
    /// The reference is attached to a `threshold("<id>")` used by the template prose.
    ThresholdRegistry,
}

/// Verification state of a template law reference.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
pub enum TemplateLawReferenceVerification {
    /// The citation is structurally derived from local metadata, but the legal article text/value
    /// has not been lawyer-verified for this template.
    Pending,
}

/// A bounded, structured citation candidate for a template.
#[derive(Debug, Clone, PartialEq, Eq, Serialize)]
pub struct TemplateLawReference {
    /// Stable source id used by the law shelf where available (`csc`, `cc`, `dl-268-94`, ...).
    pub source_id: String,
    /// Human-readable source label.
    pub source_label: String,
    /// Article number when a single article is known; omitted for whole-diploma or multi-article
    /// references.
    pub article: Option<String>,
    /// Human-readable citation, preserving threshold registry wording where that is the source.
    pub citation: String,
    /// How this reference was derived.
    pub source: TemplateLawReferenceSource,
    /// Honest verification state; this surface does not certify legal completeness/validity.
    pub verification: TemplateLawReferenceVerification,
    /// Threshold id when the reference came from the threshold registry.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub threshold_id: Option<String>,
}

/// One row of a [`BlockSpec::KeyValue`]: `key` and `value` are **minijinja templates** (usually
/// `key` is a literal PT label and `value` interpolates a record field). A row whose `value`
/// renders empty is omitted — this is how optional/absent fields (e.g. a missing NIPC) disappear.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct KvRowSpec {
    /// minijinja template for the row label (left column).
    pub key: String,
    /// minijinja template for the row value (right column).
    pub value: String,
}

/// A block in a [`TemplateSpec`]'s layout. Prose blocks ([`BlockSpec::Heading`],
/// [`BlockSpec::Paragraph`]) carry a **minijinja template string** rendered against the record
/// context. Structural blocks project typed record fields into rows/slots (TPL-03: no free-typing
/// of the generated document).
///
/// ## Asset schema (`assets/*.json`, `kind`-tagged)
///
/// | `kind` | fields | produces |
/// |---|---|---|
/// | `Heading` | `level:u8`, `template` | [`Block::Heading`] (skipped if it renders empty) |
/// | `Paragraph` | `template`, `items?` | one [`Block::Paragraph`] (or one per element of `items`) |
/// | `KeyValue` | `rows:[{key,value}]`, `items?` | [`Block::KeyValue`] (rows repeat per `items` element) |
/// | `VoteTable` | `items`, `label`, `vote_field?="vote"`, `unanimous_total?` | [`Block::VoteTable`] |
/// | `SignatureBlock` | `source`, `role`, `name` | [`Block::SignatureBlock`] |
/// | `PageBreak` / `Rule` | — | the matching marker block |
///
/// `items` / `source` are dotted context paths to an array; a template rendered per element sees
/// the element's fields overlaid on the parent context (so `{{ text }}` binds to the element and
/// `{{ members_present }}` still reaches the record root). **A prose block that renders to only
/// whitespace, and a structural block that yields zero rows/slots, are omitted** — this gives
/// conditional blocks (`{% if channel == 'Telematic' %}…{% endif %}`) and optional sections for
/// free, without a schema-level condition.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(tag = "kind", deny_unknown_fields)]
pub enum BlockSpec {
    /// A heading whose text is a minijinja template.
    Heading {
        /// Heading depth, 1-based.
        level: u8,
        /// minijinja template for the heading text (e.g. `"Ata n.º {{ ata_number }}"`).
        template: String,
    },
    /// A paragraph whose text is a minijinja template. When `items` is set, one paragraph is
    /// emitted per array element (each in element+parent scope); otherwise one paragraph.
    Paragraph {
        /// Optional dotted context path to an array; emit one paragraph per element.
        #[serde(default)]
        items: Option<String>,
        /// minijinja template for the paragraph body.
        template: String,
    },
    /// A key/value table. When `items` is set, `rows` repeat once per array element (in
    /// element+parent scope); otherwise `rows` render once against the record root.
    KeyValue {
        /// Optional dotted context path to an array; repeat `rows` per element.
        #[serde(default)]
        items: Option<String>,
        /// The row templates, in order.
        rows: Vec<KvRowSpec>,
    },
    /// The deliberation vote table. Projects each element of `items` into a [`VoteRow`]: the
    /// domain `VoteResult` serialized in `vote_field` becomes `{favor, against, abstain}`
    /// (`Recorded` → its counts; `Unanimous` → `favor = unanimous_total` if given, else 0).
    /// Elements with no recorded vote are skipped.
    VoteTable {
        /// Dotted context path to the deliberations array (e.g. `"deliberation_items"`).
        items: String,
        /// minijinja template for each row's label (element+parent scope).
        label: String,
        /// Field within each element carrying the serialized `VoteResult`.
        #[serde(default = "default_vote_field")]
        vote_field: String,
        /// minijinja template (→ integer) for the favour count when a vote is `Unanimous`.
        #[serde(default)]
        unanimous_total: Option<String>,
    },
    /// The signature block. Projects each element of `source` into a [`SignatureSlot`] using the
    /// `role` and `name` templates (element+parent scope). Slots that render fully empty are
    /// skipped; a source with no elements omits the block.
    SignatureBlock {
        /// Dotted context path to the signatory array (e.g. `"signatories"`).
        source: String,
        /// minijinja template for each slot's role label (e.g. `"{{ capacity | role_label }}"`).
        role: String,
        /// minijinja template for each slot's signatory name.
        name: String,
    },
    /// A forced page break.
    PageBreak,
    /// A horizontal rule.
    Rule,
}

fn default_vote_field() -> String {
    "vote".to_string()
}

// ---------------------------------------------------------------------------------------------
// Wire DTO (deserialization). `TemplateSpec` cannot derive `Deserialize` because core's
// `SignaturePolicyHint` is `Serialize`-only (e0's flagged gap). We own a local mirror of that one
// field and convert into the runtime `TemplateSpec`. Every other field either is a primitive or a
// core type that already derives `Deserialize` (`EntityFamily`, `MeetingChannel`,
// `LifecycleStage`), and `BlockSpec` derives it directly.
// ---------------------------------------------------------------------------------------------

/// Local, `Deserialize`-able mirror of [`chancela_core::SignaturePolicyHint`] (which is
/// `Serialize`-only). Variant names match the core enum verbatim so the asset JSON reads
/// naturally (`"signature_policy": "QualifiedPreferred"`).
#[derive(Debug, Clone, Copy, PartialEq, Deserialize)]
enum SignaturePolicyDto {
    QualifiedPreferred,
    QualifiedOrHandwritten,
    ManualAttested,
}

impl From<SignaturePolicyDto> for SignaturePolicyHint {
    fn from(dto: SignaturePolicyDto) -> Self {
        match dto {
            SignaturePolicyDto::QualifiedPreferred => SignaturePolicyHint::QualifiedPreferred,
            SignaturePolicyDto::QualifiedOrHandwritten => {
                SignaturePolicyHint::QualifiedOrHandwritten
            }
            SignaturePolicyDto::ManualAttested => SignaturePolicyHint::ManualAttested,
        }
    }
}

/// The on-disk asset shape. Deserialized from an `assets/*.json` file, then converted into the
/// runtime [`TemplateSpec`]. `#[serde(deny_unknown_fields)]` so a typo in an asset is a load-time
/// error, not a silently-ignored field.
#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
struct TemplateSpecDto {
    id: String,
    family: EntityFamily,
    stage: LifecycleStage,
    channels: Vec<MeetingChannel>,
    signature_policy: SignaturePolicyDto,
    rule_pack_id: String,
    blocks: Vec<BlockSpec>,
    locale: String,
    /// The editable seed body. Optional in the asset: a template with no fillable body simply
    /// omits it and gets an empty seed.
    #[serde(default)]
    default_body: Vec<DefaultBodyClause>,
}

impl From<TemplateSpecDto> for TemplateSpec {
    fn from(dto: TemplateSpecDto) -> Self {
        let law_references = derive_template_law_references(&dto.rule_pack_id, &dto.blocks);
        TemplateSpec {
            id: dto.id,
            family: dto.family,
            stage: dto.stage,
            channels: dto.channels,
            signature_policy: dto.signature_policy.into(),
            rule_pack_id: dto.rule_pack_id,
            law_references,
            blocks: dto.blocks,
            locale: dto.locale,
            default_body: dto.default_body,
        }
    }
}

fn derive_template_law_references(
    rule_pack_id: &str,
    blocks: &[BlockSpec],
) -> Vec<TemplateLawReference> {
    let mut refs = Vec::new();
    for r in rule_pack_law_references(rule_pack_id) {
        push_unique_law_reference(&mut refs, r);
    }
    for threshold_id in threshold_references_in_blocks(blocks) {
        if let Some(threshold) = find_threshold(&threshold_id)
            && let Some(r) = threshold_law_reference(threshold)
        {
            push_unique_law_reference(&mut refs, r);
        }
    }
    refs
}

fn push_unique_law_reference(
    refs: &mut Vec<TemplateLawReference>,
    reference: TemplateLawReference,
) {
    let duplicate = refs.iter().any(|r| {
        r.source_id == reference.source_id
            && r.article == reference.article
            && r.citation == reference.citation
            && r.source == reference.source
            && r.threshold_id == reference.threshold_id
    });
    if !duplicate {
        refs.push(reference);
    }
}


//! # chancela-templates — the template catalog engine (t48 / TPL-*, Wave C batch-0)
//!
//! This crate owns the template **catalog as data** and the minijinja prose rendering that turns a
//! record context into a [`chancela_core::DocumentModel`] (the frozen render↔pdf seam, §3.1).
//!
//! The document *structure* — which blocks, in what order, the signature policy (TPL-04), the
//! bound rule pack (TPL-30) — is registry data ([`TemplateSpec`] / [`BlockSpec`]); minijinja fills
//! only the *prose fragments* (the fixed legal boilerplate with `{{ field }}` holes). This keeps
//! compliance in the rule pack, never the template (WFL-31), and lets prose be locale-authored
//! content. **Adding a template is a data drop** (`assets/*.json`) — no Rust code (see the module
//! docs on `BlockSpec` for the schema and the crate log for the authoring guide).
//!
//! ## Pipeline
//!
//! ```text
//! assets/*.json ──build.rs──► ASSET_FILES ──load_registry()──► Registry
//!                                                                  │ .get(id) / .find(family,stage)
//!                                                                  ▼
//!   record ctx (serde_json::Value) ──render(spec, ctx)──► DocumentModel  (the frozen seam)
//! ```
//!
//! ## Determinism
//!
//! Rendering reads no clock and no RNG. `created_at` comes from the context or is left `None`
//! (core supplies it). Output block order follows the spec's block order and array-field order, so
//! the same `spec` + `ctx` always serialize byte-identically (regeneration/re-verification, D3).

use chancela_core::{
    Block, DocumentModel, EntityFamily, KvRow, LifecycleStage, MeetingChannel, Run,
    SignaturePolicyHint, SignatureSlot, VoteRow,
};
use serde::{Deserialize, Serialize};
use serde_json::Value;

// The embedded catalog manifest generated by `build.rs`: `ASSET_FILES: &[(&str, &str)]`
// (asset-stem, file-contents). Regenerated whenever an `assets/*.json` file changes.
include!(concat!(env!("OUT_DIR"), "/asset_manifest.rs"));

// ---------------------------------------------------------------------------------------------
// Registry data types (§3.2) — the runtime shape. `TemplateSpec` derives `Serialize` only (the
// picker DTO / `document.generated` event read it); deserialization goes through the wire DTO
// below because core's `SignaturePolicyHint` is `Serialize`-only.
// ---------------------------------------------------------------------------------------------

/// A registry entry: one template, versioned, binding a family × stage to an ordered block
/// layout (§3.2). `id` carries a version suffix (e.g. `"csc-ata-ag/v1"`); the loader rejects
/// duplicate ids. Recorded verbatim in the `document.generated` ledger event, so a later template
/// edit never changes what a past seal produced (D4).
#[derive(Debug, Clone, PartialEq, Serialize)]
pub struct TemplateSpec {
    /// Stable, versioned id (e.g. `"csc-ata-ag/v1"`). Unique across the registry.
    pub id: String,
    /// The entity family this template serves.
    pub family: EntityFamily,
    /// Where in the book lifecycle this template sits.
    pub stage: LifecycleStage,
    /// The meeting channels this template is valid for.
    pub channels: Vec<MeetingChannel>,
    /// The signature policy hint to prefer for documents rendered from this template (TPL-04).
    pub signature_policy: SignaturePolicyHint,
    /// The compliance rule pack bound to this template (TPL-30); the id, not the pack itself.
    pub rule_pack_id: String,
    /// The ordered block layout that produces the [`DocumentModel`].
    pub blocks: Vec<BlockSpec>,
    /// The locale the prose is authored in (v1: `"pt-PT"`, UX-21).
    pub locale: String,
}

/// One row of a [`BlockSpec::KeyValue`]: `key` and `value` are **minijinja templates** (usually
/// `key` is a literal PT label and `value` interpolates a record field). A row whose `value`
/// renders empty is omitted — this is how optional/absent fields (e.g. a missing NIPC) disappear.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct KvRowSpec {
    /// minijinja template for the row label (left column).
    pub key: String,
    /// minijinja template for the row value (right column).
    pub value: String,
}

/// A block in a [`TemplateSpec`]'s layout. Prose blocks ([`BlockSpec::Heading`],
/// [`BlockSpec::Paragraph`]) carry a **minijinja template string** rendered against the record
/// context. Structural blocks project typed record fields into rows/slots (TPL-03: no free-typing
/// of the generated document).
///
/// ## Asset schema (`assets/*.json`, `kind`-tagged)
///
/// | `kind` | fields | produces |
/// |---|---|---|
/// | `Heading` | `level:u8`, `template` | [`Block::Heading`] (skipped if it renders empty) |
/// | `Paragraph` | `template`, `items?` | one [`Block::Paragraph`] (or one per element of `items`) |
/// | `KeyValue` | `rows:[{key,value}]`, `items?` | [`Block::KeyValue`] (rows repeat per `items` element) |
/// | `VoteTable` | `items`, `label`, `vote_field?="vote"`, `unanimous_total?` | [`Block::VoteTable`] |
/// | `SignatureBlock` | `source`, `role`, `name` | [`Block::SignatureBlock`] |
/// | `PageBreak` / `Rule` | — | the matching marker block |
///
/// `items` / `source` are dotted context paths to an array; a template rendered per element sees
/// the element's fields overlaid on the parent context (so `{{ text }}` binds to the element and
/// `{{ members_present }}` still reaches the record root). **A prose block that renders to only
/// whitespace, and a structural block that yields zero rows/slots, are omitted** — this gives
/// conditional blocks (`{% if channel == 'Telematic' %}…{% endif %}`) and optional sections for
/// free, without a schema-level condition.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(tag = "kind")]
pub enum BlockSpec {
    /// A heading whose text is a minijinja template.
    Heading {
        /// Heading depth, 1-based.
        level: u8,
        /// minijinja template for the heading text (e.g. `"Ata n.º {{ ata_number }}"`).
        template: String,
    },
    /// A paragraph whose text is a minijinja template. When `items` is set, one paragraph is
    /// emitted per array element (each in element+parent scope); otherwise one paragraph.
    Paragraph {
        /// Optional dotted context path to an array; emit one paragraph per element.
        #[serde(default)]
        items: Option<String>,
        /// minijinja template for the paragraph body.
        template: String,
    },
    /// A key/value table. When `items` is set, `rows` repeat once per array element (in
    /// element+parent scope); otherwise `rows` render once against the record root.
    KeyValue {
        /// Optional dotted context path to an array; repeat `rows` per element.
        #[serde(default)]
        items: Option<String>,
        /// The row templates, in order.
        rows: Vec<KvRowSpec>,
    },
    /// The deliberation vote table. Projects each element of `items` into a [`VoteRow`]: the
    /// domain `VoteResult` serialized in `vote_field` becomes `{favor, against, abstain}`
    /// (`Recorded` → its counts; `Unanimous` → `favor = unanimous_total` if given, else 0).
    /// Elements with no recorded vote are skipped.
    VoteTable {
        /// Dotted context path to the deliberations array (e.g. `"deliberation_items"`).
        items: String,
        /// minijinja template for each row's label (element+parent scope).
        label: String,
        /// Field within each element carrying the serialized `VoteResult`.
        #[serde(default = "default_vote_field")]
        vote_field: String,
        /// minijinja template (→ integer) for the favour count when a vote is `Unanimous`.
        #[serde(default)]
        unanimous_total: Option<String>,
    },
    /// The signature block. Projects each element of `source` into a [`SignatureSlot`] using the
    /// `role` and `name` templates (element+parent scope). Slots that render fully empty are
    /// skipped; a source with no elements omits the block.
    SignatureBlock {
        /// Dotted context path to the signatory array (e.g. `"signatories"`).
        source: String,
        /// minijinja template for each slot's role label (e.g. `"{{ capacity | role_label }}"`).
        role: String,
        /// minijinja template for each slot's signatory name.
        name: String,
    },
    /// A forced page break.
    PageBreak,
    /// A horizontal rule.
    Rule,
}

fn default_vote_field() -> String {
    "vote".to_string()
}

// ---------------------------------------------------------------------------------------------
// Wire DTO (deserialization). `TemplateSpec` cannot derive `Deserialize` because core's
// `SignaturePolicyHint` is `Serialize`-only (e0's flagged gap). We own a local mirror of that one
// field and convert into the runtime `TemplateSpec`. Every other field either is a primitive or a
// core type that already derives `Deserialize` (`EntityFamily`, `MeetingChannel`,
// `LifecycleStage`), and `BlockSpec` derives it directly.
// ---------------------------------------------------------------------------------------------

/// Local, `Deserialize`-able mirror of [`chancela_core::SignaturePolicyHint`] (which is
/// `Serialize`-only). Variant names match the core enum verbatim so the asset JSON reads
/// naturally (`"signature_policy": "QualifiedPreferred"`).
#[derive(Debug, Clone, Copy, PartialEq, Deserialize)]
enum SignaturePolicyDto {
    QualifiedPreferred,
    QualifiedOrHandwritten,
    ManualAttested,
}

impl From<SignaturePolicyDto> for SignaturePolicyHint {
    fn from(dto: SignaturePolicyDto) -> Self {
        match dto {
            SignaturePolicyDto::QualifiedPreferred => SignaturePolicyHint::QualifiedPreferred,
            SignaturePolicyDto::QualifiedOrHandwritten => {
                SignaturePolicyHint::QualifiedOrHandwritten
            }
            SignaturePolicyDto::ManualAttested => SignaturePolicyHint::ManualAttested,
        }
    }
}

/// The on-disk asset shape. Deserialized from an `assets/*.json` file, then converted into the
/// runtime [`TemplateSpec`]. `#[serde(deny_unknown_fields)]` so a typo in an asset is a load-time
/// error, not a silently-ignored field.
#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
struct TemplateSpecDto {
    id: String,
    family: EntityFamily,
    stage: LifecycleStage,
    channels: Vec<MeetingChannel>,
    signature_policy: SignaturePolicyDto,
    rule_pack_id: String,
    blocks: Vec<BlockSpec>,
    locale: String,
}

impl From<TemplateSpecDto> for TemplateSpec {
    fn from(dto: TemplateSpecDto) -> Self {
        TemplateSpec {
            id: dto.id,
            family: dto.family,
            stage: dto.stage,
            channels: dto.channels,
            signature_policy: dto.signature_policy.into(),
            rule_pack_id: dto.rule_pack_id,
            blocks: dto.blocks,
            locale: dto.locale,
        }
    }
}

